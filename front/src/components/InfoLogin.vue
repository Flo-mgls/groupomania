<template>
  <div>
    <div class="row mb-2">
      <div class="col-lg-4 col-md-8 offset-lg-4 offset-md-2">
        <input
          class="form-control text-center"
          type="email"
          placeholder="Votre email"
          id="email"
          required
          maxlength="60"
          v-model="email"
          v-on:input="sendData"
        />
      </div>
    </div>
    <div class="row mb-2">
      <div class="col-lg-4 col-md-8 offset-lg-4 offset-md-2">
        <input
          class="form-control text-center"
          type="password"
          placeholder="Votre mot de passe"
          id="password"
          required
          pattern="(?=.*\d)(?=.*[a-z])(?=.*[A-Z]).{8,}"
          v-model="password"
          v-on:input="sendData"
        />
      </div>
    </div>
    <div class="row mb-2">
      <div class="col-lg-4 col-md-8 offset-lg-4 offset-md-2">
        <button
          class="btn btn-light form-control text-center"
          type="submit"
          v-on:click="sendRequest"
        >{{ validateText }}</button>
        <p class="text-muted">
          <slot name="messagePassword"></slot>
        </p>
        <p class="text-danger">
          <slot name="messageError"></slot>
        </p>
      </div>
    </div>
  </div>
</template>

<script>
export default {
  name: "InfoLogin",
  props: ["validateText"],
  data: () => {
    return {
      email: "",
      password: "",
    };
  },
  methods: {
    sendData() {
      this.$emit("data-sent", this.$data);
    },
    sendRequest() {
      const emailValid = document.getElementById("email").checkValidity();
      const passwordValid = document.getElementById("password").checkValidity();
      if (emailValid && passwordValid) {
        this.$emit("request-sent");
      }
    },
  },
};
</script>

